import { IconSearch as SearchIcon } from "@tabler/icons-react"

import { cn } from "@/lib/utils"
import {
  Dialog,
  DialogContent,
  DialogDescription,
  DialogHeader,
  DialogTitle,
} from "@/components/ui/dialog"


function Command({
  className,
  ...props
}: React.ComponentProps<typeof CommandPrimitive>) {
  return (
    <CommandPrimitive
      data-slot="command"
      className={cn(
        "bg-popover text-popover-foreground flex h-full w-full flex-col overflow-hidden rounded-md",
        className
      )}
      {...props}
    />
  )
}

function CommandDialog({
  title = "Command Palette",
  description = "Search for a command to run...",
  children,
  className,
  showCloseButton = false,
  itemsMap = new Map(),
  ...props
}: React.ComponentProps<typeof Dialog> & {
  title?: string
  description?: string
  className?: string
  showCloseButton?: boolean
  itemsMap?: Map<string, { title: string; type: string }>
}) {

    const customFilter = (value: string, search: string) => {
    const item = itemsMap.get(value);
    if (!item) return 0;

    const [prefix, query] = search.split(":");

    // Handle prefixed search like "blog:react" or "project:e-commerce"
    if (query !== undefined) {
      if (item.type.toLowerCase() === prefix.toLowerCase()) {
        return item.title.toLowerCase().includes(query.toLowerCase()) ? 1 : 0;
      }
      return 0; // Don't show if the type doesn't match the prefix
    }

    // Handle non-prefixed search
    return item.title.toLowerCase().includes(search.toLowerCase()) ? 1 : 0;
  };
  return (
    <Dialog {...props}>
      <DialogHeader className="sr-only">
        <DialogTitle>{title}</DialogTitle>
        <DialogDescription>{description}</DialogDescription>
      </DialogHeader>
      <DialogContent
        className={cn("bg-background data-[state=open]:animate-in data-[state=closed]:animate-out data-[state=closed]:fade-out-0 data-[state=open]:fade-in-0 data-[state=closed]:zoom-out-95 data-[state=open]:zoom-in-95 fixed top-[50%] left-[50%] z-50 grid w-full max-w-[calc(100%-2rem)] translate-x-[-50%] translate-y-[-50%] gap-4 border duration-200 sm:max-w-lg rounded-xl border-none bg-clip-padding p-2 shadow-2xl ring-4 ring-neutral-200/80 dark:bg-neutral-900 dark:ring-neutral-800", className)}
        showCloseButton={showCloseButton}
      >
        <Command filter={customFilter} className="text-popover-foreground flex h-full w-full flex-col overflow-hidden **:data-[slot=command-input-wrapper]:bg-input/50 **:data-[slot=command-input-wrapper]:border-input rounded-none bg-transparent **:data-[slot=command-input]:!h-9 **:data-[slot=command-input]:py-0 **:data-[slot=command-input-wrapper]:mb-0 **:data-[slot=command-input-wrapper]:!h-9 **:data-[slot=command-input-wrapper]:rounded-md **:data-[slot=command-input-wrapper]:border">
          {children}
        </Command>
      </DialogContent>
    </Dialog>
  )
}

